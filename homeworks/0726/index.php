    var_dump($arr[$i]);
}

////////////////////////////////////////////////////////////////////////////////////////////////////////
// Funkciją, kuri apverstų masyvo elementus;
////////////////////////////////////////////////////////////////////////////////////////////////////////
function reversArray($arr)
{
    if (is_array($arr)) {
        return array_reverse($arr);
    }
}

var_dump(reversArray($arr));

////////////////////////////////////////////////////////////////////////////////////////////////////////
// Sukurti Fibonacci seką(tai tokia seka, kuomet sekantis skaičius yra sudedamas su prieš tai einančiu.)
////////////////////////////////////////////////////////////////////////////////////////////////////////
$zero = 0;
$firstValue = $arr[0];

for($i = 0; $i < count($arr); $i++)
{
    $sumValues = $zero + $firstValue;
    var_dump($sumValues);
    $zero = $firstValue;
    $firstValue = $sumValues;
}

////////////////////////////////////////////////////////////////////////////////////////////////////////
// Išvesti visų masyvo elementų vidurkį;(foreach)
////////////////////////////////////////////////////////////////////////////////////////////////////////
$sum = 0;
$count = 0;

foreach ($arr as $value) {
    $sum += $value;
    $count++;
}

var_dump($sum/$count);

